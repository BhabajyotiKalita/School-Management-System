int getNextdepartmentid()
{
    int D_id=1;

    try
    {
        PreparedStatement PS=con.prepareStatement("select max(D_id) from department");
        ResultSet rs=PS.executeQuery();
        if(rs.next())
        {
            D_id=Integer.parseInt(rs.getString(1)) +1;

        }
        else
        {
            D_id=1;
        }
    }
    catch(Exception e)
    {
        D_id=1;
    }

    return D_id;

}

void populateApplicationData()
{

    try
    {
        PreparedStatement PS=con.prepareStatement("select * from department where D_id='" + jTextField6.getText() +"'");
        ResultSet rs=PS.executeQuery();
        if(rs.next())
        {
            jTextField1.setText(rs.getString("D_id"));
            jTextField2.setText(rs.getString("D_name"));
            jTextField3.setText(rs.getString("D_teacher"));
            jTextField4.setText(rs.getString("D_student"));

        }


    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(new JPanel(),e,"Error 111",1);

    }
}
